#!/usr/bin/env python3
"""
FROZEN benchmark harness for autoresearch-cublas-sam3.

DO NOT EDIT THIS FILE.  The AI agent must only modify tune_config.py.

Measures GEMM throughput across SAM3 shapes using the tuning configuration
from tune_config.py.  Works on both NVIDIA (cuBLASLt) and AMD (hipBLASLt).

SAM3 shapes extracted from facebookresearch/sam3 source code.

Outputs a single scalar score (negative weighted TFLOP/s).  Lower is better.
"""

import json
import os
import sys
from dataclasses import dataclass

import torch
import tune_config


# ---------------------------------------------------------------------------
# Fixed GEMM shapes from Meta SAM3 (Segment Anything Model 3)
# ---------------------------------------------------------------------------

@dataclass
class GemmShape:
    M: int
    N: int
    K: int
    label: str
    weight: float


SHAPES = [
    # --- TIER 1: Highest FLOP, dominate inference time ---
    GemmShape( 5184, 36288,  256, "trk_xattn_qk",     0.12),
    GemmShape( 5184,   256, 36288, "trk_xattn_v",      0.12),
    GemmShape( 5184,  4736, 1024, "vit_g_mlp_up",      0.08),
    GemmShape( 5184,  1024, 4736, "vit_g_mlp_down",    0.08),
    GemmShape( 5184,  3072, 1024, "vit_g_qkv",         0.06),
    # --- TIER 2: Medium FLOP ---
    GemmShape( 5184,  5184,  256, "trk_sattn_qk",      0.05),
    GemmShape( 5184,   256, 5184, "trk_sattn_v",       0.05),
    GemmShape( 5184,  1024, 1024, "vit_g_out",         0.04),
    GemmShape(  576,  4736, 1024, "vit_w_mlp_up",      0.06),
    GemmShape(  576,  1024, 4736, "vit_w_mlp_down",    0.06),
    GemmShape(  576,  3072, 1024, "vit_w_qkv",         0.04),
    # --- TIER 3: Detector ---
    GemmShape( 5184,  2048,  256, "det_enc_ffn_up",    0.03),
    GemmShape( 5184,   256, 2048, "det_enc_ffn_down",  0.03),
    GemmShape(  400,  5184,  256, "det_xattn_qk",      0.02),
    # --- TIER 4: Text encoder ---
    GemmShape(   32,  4096, 1024, "txt_mlp_up",        0.01),
    GemmShape(   32,  1024, 4096, "txt_mlp_down",      0.01),
]

WARMUP_ITERS = 10
BENCH_ITERS = 50


# ---------------------------------------------------------------------------
# GPU detection
# ---------------------------------------------------------------------------

def detect_backend():
    device_name = torch.cuda.get_device_name(0).lower()
    if "amd" in device_name or "mi" in device_name or "radeon" in device_name:
        return "amd"
    if hasattr(torch.version, "hip") and torch.version.hip is not None:
        return "amd"
    return "nvidia"


# ---------------------------------------------------------------------------
# Benchmark one shape
# ---------------------------------------------------------------------------

def benchmark_shape(shape: GemmShape) -> dict:
    """Benchmark a single GEMM shape with all tune_config settings applied."""
    M_orig, N_orig, K_orig = shape.M, shape.N, shape.K
    dtype = tune_config.get_dtype()

    # --- Padding ---
    M_pad, N_pad, K_pad = tune_config.get_padded_shape(M_orig, N_orig, K_orig)
    used_padding = (M_pad != M_orig or N_pad != N_orig or K_pad != K_orig)

    # --- Batched window GEMM ---
    use_batch = tune_config.should_batch_windows(M_orig, N_orig, K_orig)
    batch_size = tune_config.WINDOW_BATCH_SIZE if use_batch else 1

    # --- Transpose B ---
    transpose_b = tune_config.should_transpose_b(M_orig, N_orig, K_orig)

    # --- Create tensors ---
    if use_batch:
        a = torch.randn(batch_size, M_pad, K_pad, device="cuda", dtype=dtype)
        if transpose_b:
            b = torch.randn(batch_size, N_pad, K_pad, device="cuda", dtype=dtype)
        else:
            b = torch.randn(batch_size, K_pad, N_pad, device="cuda", dtype=dtype)
    else:
        a = torch.randn(M_pad, K_pad, device="cuda", dtype=dtype)
        if transpose_b:
            b = torch.randn(N_pad, K_pad, device="cuda", dtype=dtype)
        else:
            b = torch.randn(K_pad, N_pad, device="cuda", dtype=dtype)

    # --- Pick the right matmul call ---
    if use_batch:
        if transpose_b:
            matmul_fn = lambda: torch.bmm(a, b.transpose(-2, -1))
        else:
            matmul_fn = lambda: torch.bmm(a, b)
    else:
        if transpose_b:
            matmul_fn = lambda: torch.mm(a, b.t())
        else:
            matmul_fn = lambda: torch.matmul(a, b)

    # --- Warmup ---
    for _ in range(WARMUP_ITERS):
        _ = matmul_fn()
    torch.cuda.synchronize()

    # --- Timed runs ---
    start_events = [torch.cuda.Event(enable_timing=True) for _ in range(BENCH_ITERS)]
    end_events = [torch.cuda.Event(enable_timing=True) for _ in range(BENCH_ITERS)]

    for i in range(BENCH_ITERS):
        start_events[i].record()
        _ = matmul_fn()
        end_events[i].record()

    torch.cuda.synchronize()

    times_ms = sorted(s.elapsed_time(e) for s, e in zip(start_events, end_events))
    median_ms = times_ms[len(times_ms) // 2]

    # FLOP count uses ORIGINAL dimensions (padding is overhead, not useful work).
    # For batched: multiply by batch_size.
    flops = 2.0 * M_orig * N_orig * K_orig * batch_size
    tflops = flops / (median_ms * 1e-3) / 1e12 if median_ms > 0 else 0.0

    # Theoretical peak (rough): RTX 3090 = ~142 TFLOP/s FP16 Tensor
    # MI300X = ~1300 TFLOP/s FP16
    # We compute % of peak for context.
    try:
        props = torch.cuda.get_device_properties(0)
        # Rough FP16 peak estimate: SM count * clock * ops_per_clock
        # This is approximate; exact depends on tensor core usage.
        peak_tflops = None  # Set below if we can estimate
    except Exception:
        peak_tflops = None

    return {
        "label": shape.label,
        "M": M_orig, "N": N_orig, "K": K_orig,
        "M_pad": M_pad, "N_pad": N_pad, "K_pad": K_pad,
        "padded": used_padding,
        "batched": use_batch,
        "batch_size": batch_size,
        "transpose_b": transpose_b,
        "dtype": str(dtype).split(".")[-1],
        "median_ms": round(median_ms, 4),
        "tflops": round(tflops, 2),
        "weight": shape.weight,
    }


# ---------------------------------------------------------------------------
# Score computation
# ---------------------------------------------------------------------------

def compute_score(results: list[dict]) -> float:
    weighted = sum(r["tflops"] * r["weight"] for r in results)
    total_w = sum(r["weight"] for r in results)
    return -(weighted / total_w) if total_w > 0 else 0


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    if not torch.cuda.is_available():
        print("ERROR: No GPU detected.", file=sys.stderr)
        sys.exit(1)

    # Apply global settings from tune_config
    tune_config.apply_global_settings()

    backend = detect_backend()
    blas_lib = "hipBLASLt" if backend == "amd" else "cuBLASLt"
    device_name = torch.cuda.get_device_name(0)

    print("=" * 72)
    print("autoresearch-cublas-sam3 Benchmark")
    print(f"  GPU: {device_name}")
    print(f"  Backend: {blas_lib}")
    print(f"  dtype: {tune_config.DTYPE}")
    print(f"  precision: {tune_config.MATMUL_PRECISION}")
    print(f"  allow_tf32: {tune_config.ALLOW_TF32}")
    print(f"  blas_lib: {tune_config.PREFERRED_BLAS}")
    print(f"  padding: {tune_config.PAD_TO_MULTIPLE}")
    print(f"  workspace: {tune_config.WORKSPACE_MB} MB")
    print(f"  batch_windows: {tune_config.BATCH_WINDOW_GEMMS}")
    print("=" * 72)
    print()

    results = []
    for shape in SHAPES:
        try:
            r = benchmark_shape(shape)
            results.append(r)

            flags = []
            if r["padded"]:
                flags.append(f"pad={r['M_pad']}x{r['N_pad']}x{r['K_pad']}")
            if r["batched"]:
                flags.append(f"batch={r['batch_size']}")
            if r["transpose_b"]:
                flags.append("NT")
            flag_str = f"  [{', '.join(flags)}]" if flags else ""

            print(f"  {r['label']:22s}  {r['M']:5d}x{r['N']:5d}x{r['K']:5d}"
                  f"  {r['median_ms']:8.3f} ms  {r['tflops']:7.2f} TFLOP/s"
                  f"{flag_str}")
        except Exception as e:
            print(f"  {shape.label:22s}  FAILED: {e}", file=sys.stderr)
            results.append({
                "label": shape.label,
                "M": shape.M, "N": shape.N, "K": shape.K,
                "M_pad": shape.M, "N_pad": shape.N, "K_pad": shape.K,
                "padded": False, "batched": False, "batch_size": 1,
                "transpose_b": False, "dtype": tune_config.DTYPE,
                "median_ms": float("inf"), "tflops": 0.0,
                "weight": shape.weight,
            })

    score = compute_score(results)

    print()
    print("-" * 72)
    print(f"  Weighted avg TFLOP/s: {-score:.2f}")
    print(f"  SCORE: {score:.4f}")
    print("=" * 72)

    # Save detailed results
    config_snapshot = {
        "dtype": tune_config.DTYPE,
        "matmul_precision": tune_config.MATMUL_PRECISION,
        "allow_tf32": tune_config.ALLOW_TF32,
        "preferred_blas": tune_config.PREFERRED_BLAS,
        "pad_to_multiple": tune_config.PAD_TO_MULTIPLE,
        "workspace_mb": tune_config.WORKSPACE_MB,
        "batch_windows": tune_config.BATCH_WINDOW_GEMMS,
    }
    output = {
        "score": score,
        "gpu": device_name,
        "backend": blas_lib,
        "config": config_snapshot,
        "results": results,
    }
    with open("last_result.json", "w") as f:
        json.dump(output, f, indent=2)

    print(f"\nFINAL_SCORE={score:.6f}")


if __name__ == "__main__":
    main()
